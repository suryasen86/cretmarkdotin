                <h4 class="mb--30">Keep Improving</h4>
            <p>There are many things you can improve once you've implemented a few steps. One thing that could work well is testing new ideas every month.   </p> 
        
   
            <h4 class="mb--30">Expand Network</h4>
            <p>As more and more companies implement digital marketing, there are opportunities everywhere. All you have to do is find them! </p> 
        
            <h4 class="mb--30">The Bottom Line 
</h4>
            <p>Digital marketing has become increasingly popular over the years due to its effectiveness in reaching large numbers of consumers. Because consumer demands and expectations have changed over the past decade, marketers must reevaluate the rules of engagement and test new technologies before diving in headfirst. New technology also allows us to track across channels, allowing measurement that was impossible just a short while ago.
