                                        <h4 class="mb--30">UI and UX - Same or different? </h4>
                                        <p> <a target="_blank" target="_blank" href="https://www.websolutions.com/blog/ux-vs-ui-whats-the-difference-in-web-design" target="_blank" rel="noopener noreferrer">While UI and UX designers work closely together, the two are quite different from each other in terms of the design process</a> UI design begins with the technical requirements of an interface. UX design is the process of enhancing user satisfaction by improving the usability and accessibility of a product, webpage, or app. </p>
                                        <p>Repeated elements of the design create consistency and a positive UX. Businesses consider ROI when spending money to develop user interfaces (UI, UX) for websites, software, games, and devices.
                                        </p>

                                        <p>The early 2000’s also known as the Smartphone era made things challenging for Computer UI designers. This huge shift in computing hardware led to designers having to rethink interfaces from scratch. A poor UI and UX ruins the efforts. Hence, it is important to develop, user-friendly, UI, UX experience for its dedicated end-users.
                                        </p>






