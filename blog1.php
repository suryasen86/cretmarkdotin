                                        <h3 class="mb--20">What are the Do’s and Don’ts?
                                        </h3>

                                        <h4 class="mb--30">Do’s:</h4>

                                        <li> mplement user-centric designs considering the needs and preferences of the users.</li>
                                        <li>Understand your potential customer and design accordingly.</li>
                                        <li>Consistent use of color palette for elements like buttons, text, links, icons, dividers, header, footer, hover states, etc.</li>















                                        <h4 class="mb--30">Dont’s:
                                        </h4>
                                        <li>Avoid excessive decorative animations to optimize the user experience.</li>
                                        <li>Pointless inconsistency in your UI elements.</li>
                                        <li>Avoid too many different styles, high possibilities of mix signals to end-users.</li>





