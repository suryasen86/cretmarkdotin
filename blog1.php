                                        <h3 class="mb--20">UI Trends to Look Out For in 2021
                                        </h3>
                                        <p> <a target="_blank" target="_blank" href="https://99designs.com/blog/video-animation/motion-graphics-vs-animation" target="_blank" rel="noopener noreferrer"> 3D motion graphics animations </a> are infinitely more realistic than 2D graphics, making it an engaging and interactive user experience. It allows users to get a better sense of the width and depth of products on e-commerce sites. </p>
                                        <p>Hover effects are quite transforming, it helps to navigate <a target="_blank" target="_blank" href="https://careerfoundry.com/en/blog/ui-design/8-sites-with-great-ui" target="_blank" rel="noopener noreferrer"> the website into a playful game </a>, which is a fantastic way to ensure an excellent engaging user experience with minimal effort.</p>
                                        <p>Morphing a transition-based animation technique is yet another effective way of adding a more artistic element to the page, with a bonus of increased session time.</p>

                                        <p>Anthropomorphic animations subtlety yet effectively makes the experience all the more immersive—and subconsciously puts the user at ease.</p>
                                        <p><a target="_blank" target="_blank" href="https://www.invisionapp.com/inside-design/kinetic-typography-examples" target="_blank" rel="noopener noreferrer"> Kinetic typography  </a> is adding living elements to a word or sentence, sets the mood or tone, and captivates the users’ attention. </p>







